"""[1] Даны два кортежа. Найти элементы, которые содержаться и в первом и во втором кортеже
и вывести на экран."""
_tuple1 = (1, 2, 3, "q", "w", )
_tuple2 = (1, 3, 'w', )
_list = []
for i in _tuple1:
    if i in _tuple2 and i not in _list:
        _list.append(i)
print(f'Элементы, пересекающиеся в кортежах: {_list}')
